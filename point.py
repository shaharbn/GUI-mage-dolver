class Point():
    """
    this class represent a point in a 2d graph
    x - the x-coordinate (horizontal) in pixels of the point (x=0 is the left of the screen.)
    y - the y-coordinate (vertical) in pixels of the point (y=0 is the top of the screen.)
    """
    def __init__(self, x, y):
        self.x = x
        self.y = y
        